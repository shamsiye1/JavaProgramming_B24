package day27_NestedLoops;

public class NestedLoopExample2 {
    public static void main(String[] args) {

       for (int i=0; i <5; i++){
           System.out.println("a"+ i);

           for (int j=0; j<i; j++){
               System.out.println("b"+j);

           }
       }














































































    }
}
