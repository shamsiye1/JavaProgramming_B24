package day07_Unary_Operator;

public class DecrementExample {
    public static void main(String[] args) {
        int n=20;
        int t=n;
        System.out.println("t: "+t);

        int j= --n;   //19
        System.out.println("j: "+j);
        System.out.println("n: "+n);

        System.out.println("n: "+ --n);
        System.out.println("n: "+ n--);
        System.out.println(n);
        System.out.println(n);
        System.out.println(n);






























    }
}
