package day19_stringMethods;

public class MultipleIndexNumber {
    public static void main(String[] args) {
        String a = "aaba";
        System.out.println(a.indexOf('a'));
        System.out.println(a.indexOf('a',a.indexOf('a')+1));

        System.out.println(a.indexOf('a',a.indexOf('a'+1)));















































































    }
}
