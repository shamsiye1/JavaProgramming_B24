package day24_loops;

public class ForLoopExample {
    public static void main(String[] args) {

        for (int i=1; i<=10; i++){
            System.out.println("good to answer"+i);
        }

      //  System.out.println(i);        ===> this [i] is not visible
        int number=5;

        for (int i=1; i<=10; ++i){
            System.out.println(number+" x "+i+" = "+(number*i));
        }


























































































    }
}
