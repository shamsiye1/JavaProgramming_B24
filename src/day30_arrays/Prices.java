package day30_arrays;

public class Prices {
    public static void main(String[] args) {
       double[] prices = {90.5,40.25,140.41};

        for (int i=0; i< prices.length; i++){

            System.out.println("$"+prices[i]);
        }

        for (double each : prices){

            System.out.println("$"+each);

        }


























    }
}
